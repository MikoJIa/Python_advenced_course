def josephus(n, k):
    res = [x for x in range(1, n + 1)]
    while len(res) > 1:
        kill = k
        if k > len(res):
            if k % len(res) != 0:
                kill = k % len(res)
            else:
                kill = len(res)
        res = res[kill:] + res[:kill - 1]
    return res


