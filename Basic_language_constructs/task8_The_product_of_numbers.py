def product_of_numbers(n: int) -> str:
    ls = []
    if 0 < n < 1000:
        while len(ls) != n + 1:
            num = int(input())
            ls.append(num)
    for i in range(0, n - 1):
        for j in range(i + 1, n):
            if i != j:
                if ls[i] * ls[j] == ls[-1]:
                    return '–î–ê'
    return '–ù–ï–¢'



print(product_of_numbers(int(input())))


