def shift_dev(num: list[int], steps=0) -> list[int]:
    if steps < 0:
        steps = abs(steps)
        for i in range(steps):
            num.append(num.pop(0))
        return num
    else:
        for i in range(steps):
            num.insert(0, num.pop())
        return num


res = list(map(int, input().split()))
