def vice_versa(num) -> int:
    lst = ''
    i = 0
    num = [int(x) for x in num]
    if len(num) % 2 == 1:
        while i != len(num[:-1]):
            num[i], num[i+1] = num[i+1], num[i]
            i += 2
        lst = num
    elif len(num) % 2 == 0:
        while i != len(num):
            num[i], num[i + 1] = num[i + 1], num[i]
            i += 2
        lst = num
    elif num:
        return lst
    return lst


